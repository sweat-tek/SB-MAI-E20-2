package org.jhotdraw.draw.action.behaviorTest;

import com.tngtech.jgiven.Stage;
import com.tngtech.jgiven.annotation.BeforeStage;
import com.tngtech.jgiven.annotation.ProvidedScenarioState;
import org.jhotdraw.draw.DefaultDrawingEditor;
import org.jhotdraw.draw.DefaultDrawingView;
import org.jhotdraw.draw.DrawingEditor;
import org.jhotdraw.draw.DrawingView;
import org.jhotdraw.draw.QuadTreeDrawing;
import static org.junit.Assert.assertTrue;

/**
 *
 * @author Simon Holland Flarup
 */
class GivenViewToZoom extends Stage<GivenViewToZoom>{
    
    
    @ProvidedScenarioState
    private DrawingEditor editor;

    @BeforeStage
    private void before() {
        editor = new DefaultDrawingEditor();
        DrawingView view = new DefaultDrawingView();
        view.setDrawing(new QuadTreeDrawing());
        editor.setActiveView(view);
    }
    
    public GivenViewToZoom view_to_zoom()
    {
        assertTrue(editor.getActiveView().getScaleFactor() == 1);
        return this;
    }
}
